			$('.priceTabButton').click(function(){
				$('.selected').removeClass('selected');
				$(this).addClass('selected');
				if($("#tabButtonPersonal").hasClass('selected')) {
					$("#tablePersonal").addClass("openTable");
					$("#tableSpecific").removeClass("openTable");
				};
				if($("#tabButtonSpe").hasClass('selected')) {
					$("#tableSpecific").addClass("openTable");
					$("#tablePersonal").removeClass("openTable");
